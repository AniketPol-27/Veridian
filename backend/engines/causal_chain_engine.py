class GeminiInterface:
    """
    Wrapper around Gemini for causal chain reasoning.
    Uses google-genai for direct API calls.
    """

    def __init__(self):
        self.model_name = os.getenv("GEMINI_MODEL", "gemini-2.5-pro")
        self.temperature = float(os.getenv("GEMINI_TEMPERATURE", "0.3"))
        self.max_tokens = int(os.getenv("GEMINI_MAX_TOKENS", "8192"))
        self._client = None

    def _get_client(self):
        if self._client is None:
            import os
            from google import genai
            api_key = os.getenv("GEMINI_API_KEY")
            if api_key:
                self._client = genai.Client(api_key=api_key)
            else:
                self._client = genai.Client()
        return self._client

    async def generate(self, prompt: str, temperature: float | None = None) -> str:
        """Generate a response from Gemini."""
        import asyncio

        client = self._get_client()
        temp = temperature if temperature is not None else self.temperature

        try:
            loop = asyncio.get_event_loop()
            response = await loop.run_in_executor(
                None,
                lambda: client.models.generate_content(
                    model=self.model_name,
                    contents=prompt,
                    config={
                        "temperature": temp,
                        "max_output_tokens": self.max_tokens,
                    },
                ),
            )
            return response.text or ""
        except Exception as e:
            logger.error(
                "gemini.generate_error",
                error=str(e),
                prompt_length=len(prompt),
            )
            raise

