class CausalChainEngine:
    """
    The 7-step causal chain protocol.

    Usage:
        engine = CausalChainEngine()
        chain = await engine.build_chain(trigger_signal_id, trigger_content)
        if chain:
            print(chain.to_summary())
    """

    def __init__(self):
        self.mcp = VeridianMCPClient()
        self.gemini = GeminiInterface()

    async def build_chain(
        self,
        trigger_signal_id: str,
        trigger_content: str,
        trigger_entities: list[str] | None = None,
    ) -> CausalChain | None:
        """
        Execute the full 7-step protocol on a trigger signal.

        Returns a CausalChain if the signal is anomalous and
        a hypothesis survives all testing, or None if the signal
        is noise or no hypothesis survives.
        """
        chain_id = f"VRD-CHN-{uuid.uuid4().hex[:8].upper()}"
        audit_trail: list[AuditStep] = []
        step_num = 0

        logger.info(
            "chain_engine.build_start",
            chain_id=chain_id,
            trigger_signal_id=trigger_signal_id,
            trigger_entities=trigger_entities,
        )

        # ══════════════════════════════════════════════
        # STEP 1: ANOMALY CONFIRMATION
        # Is this signal statistically anomalous?
        # ══════════════════════════════════════════════
        step_num += 1
        logger.info("chain_engine.step", step=1, name="anomaly_confirmation")

        primary_entity = (trigger_entities[0] if trigger_entities else None)
        anomaly_result = None

        if primary_entity:
            try:
                anomaly_result = await self.mcp.compute_entity_anomaly(
                    entity_name=primary_entity,
                    metric="impact_score",
                    lookback_days=90,
                    current_window_days=7,
                )
            except Exception as e:
                logger.warning("chain_engine.anomaly_check_failed", error=str(e))

        # For hackathon demo: even if anomaly check fails or returns
        # non-anomalous, proceed with chain building if we have entities.
        # In production, we'd stop here for non-anomalous signals.
        is_anomalous = (
            anomaly_result is not None and anomaly_result.is_anomalous
        ) or (primary_entity is not None)

        audit_trail.append(AuditStep(
            step_number=step_num,
            step_name="anomaly_confirmation",
            tool_used="mcp:compute_entity_anomaly",
            input_summary=f"Entity: {primary_entity or 'none'}, Metric: impact_score",
            output_summary=(
                anomaly_result.format_for_prompt() if anomaly_result
                else "No entity to check — proceeding based on signal content"
            ),
            reasoning=(
                f"Z-score: {anomaly_result.z_score:.2f}, "
                f"Anomalous: {anomaly_result.is_anomalous}" if anomaly_result
                else "Proceeding without anomaly score — entity-based trigger"
            ),
        ).to_dict())

        if not is_anomalous:
            logger.info("chain_engine.not_anomalous", chain_id=chain_id)
            return None

        # ══════════════════════════════════════════════
        # STEP 2: EVIDENCE RETRIEVAL
        # What related signals exist in memory?
        # ══════════════════════════════════════════════
        step_num += 1
        logger.info("chain_engine.step", step=2, name="evidence_retrieval")

        # 2a: Find semantically related signals
        related_signals = await self.mcp.find_related_signals(
            query=trigger_content[:300],
            time_range_days=90,
            min_score=0.5,
            limit=20,
            exclude_signal_ids=[trigger_signal_id],
        )

        # 2b: Get entity state (if entity exists)
        entity_state = None
        if primary_entity:
            try:
                entity_state = await self.mcp.get_entity_state(
                    entity_name=primary_entity,
                    dimensions=["news", "community", "financial", "hiring"],
                    lookback_days=90,
                )
            except Exception as e:
                logger.warning("chain_engine.entity_state_failed", error=str(e))

        # 2c: Find historical pattern matches
        signal_ids_for_pattern = [trigger_signal_id]
        if related_signals.signals:
            signal_ids_for_pattern.extend(
                [s.signal_id for s in related_signals.signals[:4]]
            )
        historical_patterns = await self.mcp.find_historical_pattern(
            current_signal_ids=signal_ids_for_pattern,
            top_k=5,
        )

        audit_trail.append(AuditStep(
            step_number=step_num,
            step_name="evidence_retrieval",
            tool_used="mcp:find_related_signals + get_entity_state + find_historical_pattern",
            input_summary=f"Trigger: {trigger_content[:100]}...",
            output_summary=(
                f"Related signals: {related_signals.total_found}, "
                f"Entity state: {'retrieved' if entity_state else 'unavailable'}, "
                f"Historical patterns: {len(historical_patterns.matches)}"
            ),
            reasoning=(
                f"Found {related_signals.total_found} semantically related signals. "
                f"{'Strong precedent exists.' if historical_patterns.has_strong_precedent else 'No strong historical precedent.'}"
            ),
        ).to_dict())

        # ══════════════════════════════════════════════
        # STEP 3: HYPOTHESIS GENERATION
        # What could explain this cluster? (Gemini)
        # ══════════════════════════════════════════════
        step_num += 1
        logger.info("chain_engine.step", step=3, name="hypothesis_generation")

        hypothesis_prompt = HYPOTHESIS_GENERATION_PROMPT.format(
            trigger_signal=trigger_content[:500],
            entity_state=(
                entity_state.format_for_prompt() if entity_state
                else "No entity state available."
            ),
            signal_count=len(related_signals.signals),
            evidence_signals=_format_signals_for_prompt(related_signals.signals[:15]),
            pattern_count=len(historical_patterns.matches),
            historical_patterns=(
                historical_patterns.format_for_prompt() if historical_patterns.matches
                else "No historical patterns found."
            ),
        )

        hypothesis_response = await self.gemini.generate(
            hypothesis_prompt,
            temperature=0.3,
        )

        hypotheses = _parse_hypotheses(hypothesis_response)

        if not hypotheses:
            logger.warning(
                "chain_engine.no_hypotheses_generated",
                chain_id=chain_id,
            )
            audit_trail.append(AuditStep(
                step_number=step_num,
                step_name="hypothesis_generation",
                tool_used="gemini",
                input_summary=f"{len(related_signals.signals)} signals + entity state provided",
                output_summary="No valid hypotheses generated",
                reasoning="Gemini could not generate grounded hypotheses from evidence",
            ).to_dict())
            return None

        audit_trail.append(AuditStep(
            step_number=step_num,
            step_name="hypothesis_generation",
            tool_used="gemini",
            input_summary=f"{len(related_signals.signals)} signals + entity state provided",
            output_summary=f"{len(hypotheses)} hypotheses generated",
            reasoning="; ".join(
                f"H{i+1}: {h.proposed_cause[:80]}"
                for i, h in enumerate(hypotheses)
            ),
        ).to_dict())

        # ══════════════════════════════════════════════
        # STEP 4: HYPOTHESIS TESTING
        # Does evidence support or contradict?
        # ══════════════════════════════════════════════
        step_num += 1
        logger.info("chain_engine.step", step=4, name="hypothesis_testing")

        test_results: list[HypothesisTestResult] = []

        for h in hypotheses:
            # 4a: Find supporting evidence
            supporting = await self.mcp.find_related_signals(
                query=f"{h.proposed_cause} {h.proposed_effect}",
                time_range_days=90,
                min_score=0.5,
                limit=10,
                exclude_signal_ids=[trigger_signal_id],
            )

            # 4b: Find contradicting evidence
            contradicting = await self.mcp.find_related_signals(
                query=h.disconfirmation_test,
                time_range_days=90,
                min_score=0.4,
                limit=10,
                exclude_signal_ids=[trigger_signal_id],
            )

            # 4c: Gemini scores the hypothesis
            test_prompt = HYPOTHESIS_TESTING_PROMPT.format(
                center_entity=h.center_entity,
                proposed_cause=h.proposed_cause,
                proposed_effect=h.proposed_effect,
                causal_mechanism=h.causal_mechanism,
                supporting_count=len(supporting.signals),
                supporting_evidence=_format_signals_for_prompt(supporting.signals[:8]),
                contradicting_count=len(contradicting.signals),
                contradicting_evidence=_format_signals_for_prompt(contradicting.signals[:8]),
            )

            test_response = await self.gemini.generate(test_prompt, temperature=0.2)
            scores = _parse_test_scores(test_response)

            overall = scores.get("overall_score", 0.5)
            contradiction = scores.get("contradiction_severity", 0.3)
            verdict_str = scores.get("verdict", "ELIMINATED")

            survived = overall > 0.35

            test_result = HypothesisTestResult(
                hypothesis=h,
                evidence_strength=scores.get("evidence_strength", 0.5),
                consistency=scores.get("consistency", 0.5),
                contradiction_severity=contradiction,
                causal_specificity=scores.get("causal_specificity", 0.5),
                alternative_explanation_risk=scores.get("alternative_explanation_risk", 0.5),
                overall_score=overall,
                supporting_signals_found=len(supporting.signals),
                contradicting_signals_found=len(contradicting.signals),
                verdict=HypothesisVerdict.SURVIVED if survived else HypothesisVerdict.ELIMINATED,
                decisive_reason=scores.get("decisive_reason", "No reason provided"),
            )
            test_results.append(test_result)

            logger.info(
                "chain_engine.hypothesis_tested",
                hypothesis_id=h.hypothesis_id,
                overall_score=overall,
                verdict="SURVIVED" if survived else "ELIMINATED",
            )

        surviving = [t for t in test_results if t.verdict == HypothesisVerdict.SURVIVED]
        eliminated = [t for t in test_results if t.verdict == HypothesisVerdict.ELIMINATED]

        audit_trail.append(AuditStep(
            step_number=step_num,
            step_name="hypothesis_testing",
            tool_used="mcp:find_related_signals + gemini",
            input_summary=f"Tested {len(hypotheses)} hypotheses",
            output_summary=f"{len(surviving)} survived, {len(eliminated)} eliminated",
            reasoning="; ".join(
                f"{t.hypothesis.hypothesis_id}: {t.verdict.value} "
                f"(score={t.overall_score:.2f}, reason: {t.decisive_reason[:60]})"
                for t in test_results
            ),
        ).to_dict())

        if not surviving:
            logger.info(
                "chain_engine.all_hypotheses_eliminated",
                chain_id=chain_id,
            )
            return None

        # ══════════════════════════════════════════════
        # STEP 5: WINNER SELECTION
        # Which hypothesis survives testing?
        # ══════════════════════════════════════════════
        step_num += 1
        logger.info("chain_engine.step", step=5, name="winner_selection")

        winner = max(surviving, key=lambda t: t.overall_score)

        audit_trail.append(AuditStep(
            step_number=step_num,
            step_name="winner_selection",
            tool_used="deterministic_ranking",
            input_summary=f"{len(surviving)} surviving hypotheses",
            output_summary=f"Winner: {winner.hypothesis.hypothesis_id} (score={winner.overall_score:.2f})",
            reasoning=(
                f"Selected hypothesis with highest overall score. "
                f"Cause: {winner.hypothesis.proposed_cause[:100]}"
            ),
        ).to_dict())

        # ══════════════════════════════════════════════
        # STEP 6: ACTIVE DISCONFIRMATION
        # Can we BREAK the winning hypothesis?
        # ══════════════════════════════════════════════
        step_num += 1
        logger.info("chain_engine.step", step=6, name="active_disconfirmation")

        # 6a: Search for challenge evidence
        challenge_signals = await self.mcp.find_related_signals(
            query=winner.hypothesis.disconfirmation_test,
            time_range_days=180,
            min_score=0.4,
            limit=15,
        )

        # 6b: Search for expected-but-absent evidence
        expected_signals = await self.mcp.find_related_signals(
            query=f"evidence of {winner.hypothesis.proposed_effect}",
            time_range_days=90,
            min_score=0.6,
            limit=5,
        )
        expected_present = len(expected_signals.signals) > 0

        # 6c: Gemini tries to break the hypothesis
        disconfirmation_prompt = DISCONFIRMATION_PROMPT.format(
            center_entity=winner.hypothesis.center_entity,
            proposed_cause=winner.hypothesis.proposed_cause,
            proposed_effect=winner.hypothesis.proposed_effect,
            causal_mechanism=winner.hypothesis.causal_mechanism,
            confidence=winner.overall_score,
            challenge_count=len(challenge_signals.signals),
            challenge_evidence=_format_signals_for_prompt(challenge_signals.signals[:10]),
            expected_but_absent=(
                "Expected outcome signals FOUND — consistent with hypothesis."
                if expected_present
                else "Expected outcome signals NOT FOUND — hypothesis predicts "
                     "signals that do not yet exist in our data."
            ),
        )

        disconfirmation_response = await self.gemini.generate(
            disconfirmation_prompt,
            temperature=0.2,
        )
        disconfirmation = _parse_disconfirmation(disconfirmation_response)
        disconfirmation.challenge_signals_found = len(challenge_signals.signals)

        survived_disconfirmation = (
            disconfirmation.verdict != DisconfirmationVerdict.REJECTED
        )

        # If modified, update the hypothesis
        if disconfirmation.verdict == DisconfirmationVerdict.MODIFIED:
            if disconfirmation.modified_hypothesis:
                winner.hypothesis.proposed_effect = disconfirmation.modified_hypothesis

        audit_trail.append(AuditStep(
            step_number=step_num,
            step_name="active_disconfirmation",
            tool_used="mcp:find_related_signals + gemini",
            input_summary=(
                f"Challenge signals: {len(challenge_signals.signals)}, "
                f"Expected evidence: {'present' if expected_present else 'absent'}"
            ),
            output_summary=(
                f"Verdict: {disconfirmation.verdict.value}, "
                f"Confidence adj: {disconfirmation.confidence_adjustment:+.2f}"
            ),
            reasoning=(
                f"Reverse causation: {disconfirmation.reverse_causation_risk}, "
                f"Confounding: {disconfirmation.confounding_variable_risk}, "
                f"Absent evidence: {disconfirmation.absent_evidence_assessment}"
            ),
        ).to_dict())

        if not survived_disconfirmation:
            logger.info(
                "chain_engine.disconfirmation_rejected",
                chain_id=chain_id,
                killer_evidence=disconfirmation.killer_evidence,
            )
            # Still return chain for audit purposes, but mark as rejected
            pass

        # ══════════════════════════════════════════════
        # STEP 7: CONFIDENCE CALIBRATION
        # What confidence does the evidence warrant?
        # ══════════════════════════════════════════════
        step_num += 1
        logger.info("chain_engine.step", step=7, name="confidence_calibration")

        # Get historical accuracy for similar chains
        historical_accuracy = "unknown"
        if historical_patterns.matches:
            accuracies = [m.historical_accuracy for m in historical_patterns.matches if m.historical_accuracy > 0]
            if accuracies:
                historical_accuracy = f"{sum(accuracies)/len(accuracies):.0%}"

        calibration_prompt = CONFIDENCE_CALIBRATION_PROMPT.format(
            chain_summary=(
                f"Entity: {winner.hypothesis.center_entity}. "
                f"Cause: {winner.hypothesis.proposed_cause}. "
                f"Effect: {winner.hypothesis.proposed_effect}."
            ),
            evidence_strength=winner.evidence_strength,
            contradiction_severity=winner.contradiction_severity,
            causal_specificity=winner.causal_specificity,
            disconfirmation_verdict=disconfirmation.verdict.value,
            disconfirmation_adjustment=disconfirmation.confidence_adjustment,
            pattern_match_count=len(historical_patterns.matches),
            historical_accuracy=historical_accuracy,
            supporting_count=winner.supporting_signals_found,
            contradicting_count=winner.contradicting_signals_found,
        )

        calibration_response = await self.gemini.generate(
            calibration_prompt,
            temperature=0.1,
        )
        final_confidence, key_assumptions = _parse_calibration(calibration_response)

        # Deterministic override: if disconfirmation rejected, cap confidence
        if not survived_disconfirmation:
            final_confidence = min(final_confidence, 0.15)

        audit_trail.append(AuditStep(
            step_number=step_num,
            step_name="confidence_calibration",
            tool_used="gemini + deterministic_rules",
            input_summary=(
                f"Evidence: {winner.evidence_strength:.2f}, "
                f"Contradiction: {winner.contradiction_severity:.2f}, "
                f"Disconfirmation: {disconfirmation.verdict.value}"
            ),
            output_summary=f"Final confidence: {final_confidence:.2f}",
            reasoning=f"Applied 10-step calibration protocol. Key assumptions: {'; '.join(key_assumptions[:3])}",
        ).to_dict())

        # ══════════════════════════════════════════════
        # BUILD THE CHAIN
        # ══════════════════════════════════════════════
        now = datetime.now(timezone.utc)
        validate_days = winner.hypothesis.timeline_estimate_days or 30

        chain = CausalChain(
            chain_id=chain_id,
            trigger_signal_id=trigger_signal_id,
            trigger_content=trigger_content[:500],
            center_entity=winner.hypothesis.center_entity,
            proposed_cause=winner.hypothesis.proposed_cause,
            proposed_effect=winner.hypothesis.proposed_effect,
            predicted_timeline_days=winner.hypothesis.timeline_estimate_days,
            confidence=round(final_confidence, 4),
            supporting_signal_ids=list(set(
                winner.hypothesis.supporting_signal_ids
                + [s.signal_id for s in related_signals.signals[:5]]
            )),
            contradicting_signal_ids=[
                s.signal_id for s in challenge_signals.signals[:5]
            ],
            key_assumptions=key_assumptions,
            reasoning_steps=audit_trail,
            hypotheses_generated=len(hypotheses),
            hypotheses_eliminated=len(eliminated),
            survived_disconfirmation=survived_disconfirmation,
            disconfirmation_result=disconfirmation.to_dict(),
            historical_pattern_matches=[
                m.model_dump() for m in historical_patterns.matches
            ],
            entity_state=entity_state.model_dump() if entity_state else None,
            status=(
                ChainStatus.ACTIVE if survived_disconfirmation
                else ChainStatus.REJECTED
            ),
            created_at=now.isoformat(),
            validate_at=(now + timedelta(days=validate_days)).isoformat(),
        )

        # ══════════════════════════════════════════════
        # PERSIST TO ELASTICSEARCH
        # ══════════════════════════════════════════════
        try:
            from backend.es.client import get_client
            es = get_client()
            await es.index(
                index="veridian_causal_chains",
                id=chain_id,
                document=chain.to_dict(),
            )
            logger.info(
                "chain_engine.chain_stored",
                chain_id=chain_id,
                confidence=final_confidence,
            )
        except Exception as e:
            logger.error(
                "chain_engine.chain_store_error",
                chain_id=chain_id,
                error=str(e),
            )

        logger.info(
            "chain_engine.build_complete",
            chain_id=chain_id,
            center_entity=chain.center_entity,
            confidence=chain.confidence,
            hypotheses_generated=chain.hypotheses_generated,
            hypotheses_eliminated=chain.hypotheses_eliminated,
            survived_disconfirmation=chain.survived_disconfirmation,
        )

        return chain