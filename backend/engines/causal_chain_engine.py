# backend/engines/causal_chain_engine.py
"""
Veridian Causal Chain Engine — The 7-Step Protocol

This is the core intelligence of Veridian. When an anomalous signal
is detected, this engine constructs a causal chain explaining WHY
it happened, WHAT will happen next, and HOW confident we should be.

The 7 Steps:
    1. ANOMALY CONFIRMATION   — Is this signal statistically anomalous?
    2. EVIDENCE RETRIEVAL     — What related signals exist in memory?
    3. HYPOTHESIS GENERATION  — What could explain this cluster? (Gemini)
    4. HYPOTHESIS TESTING     — Does evidence support or contradict? (Elastic + Gemini)
    5. WINNER SELECTION       — Which hypothesis survives testing?
    6. ACTIVE DISCONFIRMATION — Can we BREAK the winning hypothesis?
    7. CONFIDENCE CALIBRATION — What confidence does the evidence warrant?

Critical Design Principle:
    Gemini NEVER invents facts. It reasons ONLY over what Elastic retrieved.
    Every claim must cite signal IDs. Every hypothesis must be testable.
"""

