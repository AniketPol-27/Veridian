def _parse_hypotheses(response_text: str) -> list[Hypothesis]:
    """Parse Gemini's hypothesis output into structured objects."""
    hypotheses = []
    blocks = response_text.split("---HYPOTHESIS---")

    for block in blocks[1:]:  # Skip text before first marker
        if "---END---" not in block:
            continue

        content = block.split("---END---")[0].strip()
        h = Hypothesis(
            hypothesis_id=f"H-{uuid.uuid4().hex[:6].upper()}",
            center_entity=_extract_field(content, "CENTER_ENTITY"),
            proposed_cause=_extract_field(content, "PROPOSED_CAUSE"),
            proposed_effect=_extract_field(content, "PROPOSED_EFFECT"),
            causal_mechanism=_extract_field(content, "CAUSAL_MECHANISM"),
            supporting_signal_ids=[
                s.strip() for s in _extract_field(content, "SUPPORTING_SIGNALS").split(",")
                if s.strip()
            ],
            disconfirmation_test=_extract_field(content, "DISCONFIRMATION_TEST"),
            initial_confidence=_extract_float(content, "INITIAL_CONFIDENCE", default=0.4),
            timeline_estimate_days=_extract_int(content, "TIMELINE_DAYS"),
        )

        # Validate: skip empty hypotheses
        if h.proposed_cause and h.proposed_effect:
            hypotheses.append(h)

    return hypotheses


def _parse_test_scores(response_text: str) -> dict[str, Any]:
    """Parse hypothesis test scores from Gemini response."""
    block = response_text
    if "---SCORES---" in block:
        block = block.split("---SCORES---")[1]
    if "---END---" in block:
        block = block.split("---END---")[0]

    return {
        "evidence_strength": _extract_float(block, "EVIDENCE_STRENGTH", 0.5),
        "consistency": _extract_float(block, "CONSISTENCY", 0.5),
        "contradiction_severity": _extract_float(block, "CONTRADICTION_SEVERITY", 0.3),
        "causal_specificity": _extract_float(block, "CAUSAL_SPECIFICITY", 0.5),
        "alternative_explanation_risk": _extract_float(block, "ALTERNATIVE_EXPLANATION_RISK", 0.5),
        "overall_score": _extract_float(block, "OVERALL_SCORE", 0.5),
        "verdict": _extract_field(block, "VERDICT").upper().strip(),
        "decisive_reason": _extract_field(block, "DECISIVE_REASON"),
    }


def _parse_disconfirmation(response_text: str) -> DisconfirmationResult:
    """Parse disconfirmation result from Gemini response."""
    block = response_text
    if "---DISCONFIRMATION---" in block:
        block = block.split("---DISCONFIRMATION---")[1]
    if "---END---" in block:
        block = block.split("---END---")[0]

    verdict_str = _extract_field(block, "VERDICT").upper().strip()
    if "REJECT" in verdict_str:
        verdict = DisconfirmationVerdict.REJECTED
    elif "MODIF" in verdict_str:
        verdict = DisconfirmationVerdict.MODIFIED
    else:
        verdict = DisconfirmationVerdict.STANDS

    modified_hyp = _extract_field(block, "MODIFIED_HYPOTHESIS")
    killer = _extract_field(block, "KILLER_EVIDENCE")

    return DisconfirmationResult(
        verdict=verdict,
        challenge_signals_found=0,  # Filled in by caller
        reverse_causation_risk=_extract_field(block, "REVERSE_CAUSATION_RISK"),
        confounding_variable_risk=_extract_field(block, "CONFOUNDING_VARIABLE_RISK"),
        absent_evidence_assessment=_extract_field(block, "ABSENT_EVIDENCE_ASSESSMENT"),
        scope_adjustment=_extract_field(block, "SCOPE_ADJUSTMENT"),
        modified_hypothesis=modified_hyp if modified_hyp.upper() != "NONE" else None,
        killer_evidence=killer if killer.upper() != "NONE" else None,
        confidence_adjustment=_extract_float(block, "CONFIDENCE_ADJUSTMENT", 0.0),
    )


def _parse_calibration(response_text: str) -> tuple[float, list[str]]:
    """Parse calibration result — returns (confidence, key_assumptions)."""
    block = response_text
    if "---CALIBRATION---" in block:
        block = block.split("---CALIBRATION---")[1]
    if "---END---" in block:
        block = block.split("---END---")[0]

    confidence = _extract_float(block, "FINAL_CONFIDENCE", 0.5)
    confidence = max(0.10, min(0.90, confidence))

    assumptions_raw = _extract_field(block, "KEY_ASSUMPTIONS")
    assumptions = [
        line.strip().lstrip("•").strip()
        for line in assumptions_raw.split("\n")
        if line.strip() and line.strip() != "•"
    ]

    return confidence, assumptions


def _extract_field(text: str, field_name: str) -> str:
    """Extract a field value from structured text output."""
    for line in text.split("\n"):
        if line.strip().upper().startswith(field_name.upper() + ":"):
            return line.split(":", 1)[1].strip()
    return ""


def _extract_float(text: str, field_name: str, default: float = 0.0) -> float:
    """Extract a float field value."""
    raw = _extract_field(text, field_name)
    try:
        return float(raw)
    except (ValueError, TypeError):
        return default


def _extract_int(text: str, field_name: str) -> int | None:
    """Extract an integer field value."""
    raw = _extract_field(text, field_name)
    if raw.upper() in ("UNKNOWN", "NONE", "N/A", ""):
        return None
    try:
        return int(float(raw))
    except (ValueError, TypeError):
        return None


def _format_signals_for_prompt(signals: list[SignalResult]) -> str:
    """Format signal list for inclusion in Gemini prompt."""
    if not signals:
        return "No signals found."

    lines = []
    for i, sig in enumerate(signals, 1):
        lines.append(
            f"[{i}] ID: {sig.signal_id}\n"
            f"    Source: {sig.source} | Type: {sig.signal_type}\n"
            f"    Entities: {', '.join(sig.entity_hints) or 'none'}\n"
            f"    Relevance: {sig.relevance_score:.2f} | "
            f"Impact: {sig.impact_score:.2f}\n"
            f"    Timestamp: {sig.timestamp}\n"
            f"    Content: {sig.raw_content[:250]}\n"
        )
    return "\n".join(lines)


